package com.eval.crud.data;

public class MemberDTO {
    private int id;
    private String name;
    private int age;
    private String gender;
    private int unitId;
    private int positionId;

    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }

    public int getUnitId() {
        return unitId;
    }

    public int getPositionId() {
        return positionId;
    }
}
